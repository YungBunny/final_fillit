#include "../libft.h"

int			ft_cntbspc(char *str)
{
	int		cnt;

	cnt = 0;
	while (str[cnt] == ' ' || str[cnt] == '\n' || str[cnt] == '\t' ||
			str[cnt] == '\v' || str[cnt] == '\r' || str[cnt] == '\f')
		cnt++;
	return (cnt);
}
