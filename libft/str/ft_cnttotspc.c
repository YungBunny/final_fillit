#include "../libft.h"

size_t		ft_cnttotspc(char *str)
{
	size_t i;

	i = 0;
	while (*str)
	{
		if (*str == ' ' || *str == '\n' || *str == '\v'
			|| *str == '\t' || *str == '\f' || *str == '\r')
			i++;
		str++;
	}
	return (i);
}
