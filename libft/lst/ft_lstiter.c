#include "../libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*lptr;

	lptr = lst;
	if (lptr && f)
	{
		while (lptr)
		{
			(*f)(lptr);
			lptr = lptr->next;
		}
	}
}
